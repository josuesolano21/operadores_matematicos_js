// Conversión de tiempo de segundos a minutos, horas y días

let Segundos = 7200;
let Minutos = Segundos / 60;
let Horas = Segundos / 3600;
let Dias = Segundos / 86400;

console.log("en segundos: ", Segundos)
console.log("en minutos: ", Minutos)
console.log("en horas: ", Horas)
console.log("en dias: ", Dias)